print("--------将其他数据类型转换成字典--------")
a=[
    ('name','曾兵'),
    ('age',25)
]
print(a[0][1])
b=dict(a)
print(b)
print("--------将其他数据转换成列表---------")
a=(10,20,30)
b=list(a)
print(b)
print("--------将其他数据转换成集合---------")
a=[1,2,3,4,5,6]
b=set(a)
print(b)
print("--------将其他数据转换成元祖---------")
a=[1,2,3]
b=tuple(a)
print(b)